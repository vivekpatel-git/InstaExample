public class InstaBean {

    String url;
    double latituted;
    double longituted;
    String locationName;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public double getLatituted() {
        return latituted;
    }

    public void setLatituted(double latituted) {
        this.latituted = latituted;
    }

    public double getLongituted() {
        return longituted;
    }

    public void setLongituted(double longituted) {
        this.longituted = longituted;
    }

    public String getLocationName() {
        return locationName;
    }

    public void setLocationName(String locationName) {
        this.locationName = locationName;
    }
}
